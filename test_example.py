def add_numbers(x: int, y: int) -> int:
    """
    Add two numbers together.

    Args:
        a: First number
        b: Second number

    Returns:
        Sum of the numbers
    """
    return x + y


def multiply(a: float, b: float) -> float:
    """
    Multiply two numbers.

    Args:
        x: First number
        y: Second number

    Returns:
        The product
    """
    return a * b
